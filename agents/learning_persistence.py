def persist_config_answers(
    answers: List[QuestionAnswer],
    draft_config_path: Path,
    output_config_path: Path,
    session_id: str = "",
) -> int:
    """
    Merge approved question answers into the draft config and write the result
    to output_config_path (the approved config).  Returns count of values applied.

    This does NOT overwrite the master client config — it produces a
    run-specific approved config that the agent will use on re-run.
    """
    approved = [a for a in answers if a.approved and a.answer.strip()]
    if not approved:
        # Just copy draft forward
        if draft_config_path.exists():
            output_config_path.write_text(
                draft_config_path.read_text(encoding="utf-8"), encoding="utf-8"
            )
        return 0

    merged: Dict[str, Any] = {}
    if draft_config_path.exists():
        try:
            merged = yaml.safe_load(draft_config_path.read_text(encoding="utf-8")) or {}
        except Exception as exc:
            logger.warning("Could not load draft config: %s", exc)

    applied = 0
    for answer in approved:
        # question_id format: q_portfolio_asset_class → portfolio.asset_class
        config_key = answer.question_id.removeprefix("q_").replace("_", ".", 1)
        _nested_set(merged, config_key, answer.answer.strip())
        applied += 1
        logger.info("Config answer applied: %s = %s", config_key, answer.answer.strip())

    if applied > 0:
        ts = datetime.now(timezone.utc).isoformat(timespec="seconds")
        header = f"# Approved config — generated by Onboarding Workbench {ts}\n# Session: {session_id}\n"
        output_config_path.write_text(
            header + yaml.dump(merged, default_flow_style=False, allow_unicode=True),
            encoding="utf-8",
        )
        logger.info("Approved config written to %s", output_config_path)

    return applied


def _nested_set(d: Dict[str, Any], dotted_key: str, val: Any) -> None:
    parts = dotted_key.split(".")
    for p in parts[:-1]:
        d = d.setdefault(p, {})
    d[parts[-1]] = val
